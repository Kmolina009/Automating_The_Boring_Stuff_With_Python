# Scope is the word used to describe the space that contains a varible,parameters, etc
#  Scope come in 2 forms, global and local scope
#      Global Scope ->Varibables assigned outside of any function, are Globally scoped, can be accessed from any functions scoped. Created upon start of program, destroyed on end of program
#      Local Scope  -> Variables assigned within a function are Locally Scoped, meaning they can only be accessed within there respective function.  Created upon start of function call, destroyed once the function returns
